library(leaflet)
library(leaflet.extras)

#Carga de mapas base
basemap <- leaflet() %>%
  # add different provider tiles
  addProviderTiles(
    "OpenStreetMap",
    # give the layer a name
    group = "OpenStreetMap"
    ) %>%
  addProviderTiles(
    "Esri.WorldImagery",
    group = "Satelital"
  ) %>%
  # add a layers control
  addLayersControl(
    baseGroups = c(
      "Satelital", "OpenStreetMap"
    ),
    # position it on the topleft
    position = "topleft"
  )


#Carga de icono para ubicaci√≥n
# Make a list of icons. We'll index into it based on name.
greenLeafIcon <- makeIcon(
  iconUrl = "https://github.com/MontserratJB/Ubicacion_Municipalidad/blob/main/app/monumental.png?raw=true",
  iconWidth = 30, iconHeight = 35,
  iconAnchorX = 22, iconAnchorY = 22
)

m <- basemap %>%
